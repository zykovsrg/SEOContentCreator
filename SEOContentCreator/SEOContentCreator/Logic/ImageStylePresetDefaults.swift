        """,
        size: "1536x1024"
    )

    static let all: [ImageStylePresetDefault] = [cover, illustration]

    static func makeDefaults() -> [ImageStylePreset] {
        all.map { ImageStylePreset(name: $0.name, styleText: $0.styleText, size: $0.size) }
    }

    static func matching(name: String) -> ImageStylePresetDefault? {
        all.first { $0.name == name }
    }
}
