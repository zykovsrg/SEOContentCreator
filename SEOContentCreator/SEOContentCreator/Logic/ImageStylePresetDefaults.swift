        size: "1536x1024"
    )

    static let illustration = ImageStylePresetDefault(
        name: "Стеклянный стиль — иллюстрация",
        styleText: """
        Единый стиль: аккуратная фотореалистичная 3D-модель из прозрачного стекла, чистый минималистичный премиальный предметный рендер.
        Фон: однотонный матовый приглушённо-синий (спокойный серо-голубой), без градиентов и узоров.
        Свет: мягкий равномерный студийный, лёгкое отражение под объектом, без резких теней.
