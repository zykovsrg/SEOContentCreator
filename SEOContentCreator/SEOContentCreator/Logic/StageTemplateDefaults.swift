import Foundation

struct StageTemplateContent {
    var userPromptTemplate: String
    var modelName: String = "gpt-4.1"
    var temperature: Double = 0.6
    var maxTokens: Int = 8000
}

enum StageTemplateDefaults {
    static func content(for stage: PipelineStage) -> StageTemplateContent {
        switch stage {
        case .structure:
            return StageTemplateContent(
                userPromptTemplate: """
                Подготовь структуру текста для посадочной страницы. Только структуру, без текста статьи.

                Тема: {{тема}}
                Тип статьи: {{тип}}
                Целевой объём (знаков): {{объём}}
                Направление: {{направление}}
                Данные врача: {{врач_данные}}
                Преимущества клиники: {{преимущества}}
                Приоритетные источники: {{источники_направления}}

                Используй H1 и H2 как основные уровни структуры. H3 — только там, где внутри H2 действительно нужен отдельный смысловой подраздел. Под каждым заголовком добавь короткую пометку (1–2 предложения): о чём раздел.

                Оставь только самое важное для пациента. Представь, что ты пациент, который выбирает клинику: что тебе было бы важно узнать? В интернете очень много контента, поэтому информацию нужно давать быстро и чётко. Не бойся сокращать структуру.

                Не добавляй ключевые запросы. Формат — Markdown.
                """
            )
        case .draft:
            return StageTemplateContent(
                userPromptTemplate: """
                Напиши текст строго по утверждённой структуре.

                Тема: {{тема}}
                Тип статьи: {{тип}}
                Целевой объём (знаков): {{объём}}
                Направление: {{направление}}
                Данные врача: {{врач_данные}}
                Преимущества клиники: {{преимущества}}
                Приоритетные источники: {{источники_направления}}

                Структура (план статьи):
                {{структура}}

                Правила текста:
                - В тексте должен быть заголовок, который коротко передаёт суть — самое главное, ради чего человек будет читать.
                - Подзаголовки — такие, чтобы было понятно, о чём пойдёт речь в блоке. Один абзац — одна мысль. У каждого абзаца и предложения есть цель, «воды» нет.
                - Пиши так, чтобы у читателя не оставалось вопросов, для чего он это прочитал.
                - Только факты, не оценочные суждения. Если не уверен, что информация соответствует доказательной медицине, — предупреди читателя прямо в тексте.
                - Никогда не выдумывай факты, статистику и данные исследований: их должно быть можно проверить поиском. Если сомневаешься — предупреждай. От этого зависит здоровье читателей.
                - Не больше трёх списков на весь текст.
                - Добавь раздел «## Частые вопросы» на 5–7 вопросов без дублей.
                - В конце — раздел «## Источники» на 7–10 позиций без дублей.
                - Не пиши заключение или вывод в конце текста или разделов.
                - Текст должен быть уникальным, не повторять другие тексты в интернете.

                Оставь только самое важное для пациента: представь, что ты пациент, который выбирает клинику. Не бойся сокращать текст.
                """
            )
        case .productBlocks:
            return StageTemplateContent(
                userPromptTemplate: """
                Встрой выбранные продуктовые блоки в текст, не ломая его структуру.

                Текущий текст:
                {{текущий_текст}}

                Продуктовые блоки для встраивания:
                {{продуктовые_блоки}}

                Верни полный обновлённый текст статьи.
                """
            )
        case .semanticsInText:
            return StageTemplateContent(
                userPromptTemplate: """
                Встрой ключевые запросы в текст естественно. Учитывай частотность и обязательность. \
                Допиши/поправь H1, сгенерируй Title и Description.

                Текущий текст:
                {{текущий_текст}}

                Ключевые запросы:
                {{семантика}}

                После основного текста статьи добавь блок метаданных строго в таком формате:
                ```json
                {"h1":"...","seoTitle":"...","seoDescription":"...","embeddedQueries":["..."],"notes":"..."}
                ```
                """
            )
        case .seoCheck:
            return StageTemplateContent(
                userPromptTemplate: """
                Проверь текст на соответствие SEO-требованиям. Не переписывай — верни список замечаний.

                H1: {{текущий_h1}}
                Title: {{текущий_title}}
                Description: {{текущий_description}}

                Текст:
                {{текущий_текст}}

                Ключевые запросы:
                {{семантика}}

                Верни ТОЛЬКО JSON в формате:
                ```json
                {"remarks":[{"category":"...","quote":"<точный фрагмент из текста>","suggestion":"<замена>","explanation":"<что не так и почему>"}]}
                ```
                Категории: «Заголовки», «Ключи», «Title/Description», «Объём», «Рекламность». \
                Поле quote — дословный фрагмент из текста (для поиска). Если фрагмент нужно просто удалить, оставь suggestion пустой строкой "" — не пиши слово «удалить» как замену.
                """,
                temperature: 0.3
            )
        case .factCheck:
            return StageTemplateContent(
                userPromptTemplate: """
                Проведи фактчекинг текста. Сверь медицинские утверждения с принципами доказательной медицины и алгоритмом работы с источниками, а данные клиники — со справочником. Не переписывай текст — верни список замечаний.

                Текст:
                {{текущий_текст}}

                Справочник клиники (База знаний):
                {{база_знаний}}

                Особое внимание:
                - проверяемость фактов, статистики и ссылок на исследования (ничего выдуманного);
                - соответствие российскому контексту (регистрация лекарств в РФ, протоколы Минздрава);
                - осторожность формулировок о лекарствах и БАДах;
                - расхождения с данными клиники (врачи, процедуры, цены).

                Верни ТОЛЬКО JSON в формате:
                ```json
                {"remarks":[{"category":"...","quote":"<точный фрагмент из текста>","suggestion":"<доработанный текст>","explanation":"<в чём проблема>"}]}
                ```
                Категории: «Факт», «Статистика», «Источник», «Российский контекст», «Имя врача», «Процедура», «Цена». Поле quote — дословный фрагмент из текста. Если фрагмент нужно просто удалить, оставь suggestion пустой строкой "" — не пиши слово «удалить» как замену.
                """,
                temperature: 0.3
            )
        case .finalReview:
            return StageTemplateContent(
                userPromptTemplate: """
                Проверь текст свежим редакторским взглядом. Не переписывай целиком — верни список точечных замечаний.

                Текст:
                {{текущий_текст}}

                Проверь:
                - орфографические, пунктуационные, грамматические и стилистические ошибки;
                - канцелярит, «воду» и перегруженные предложения;
                - шаблонные ИИ-фразы — текст должен звучать естественно;
                - лишнее: в тексте должно оставаться только то, что решает проблему читателя. Не бойся предлагать сокращения.

                Запрещённые формулировки (замени по образцу):
                {{запрещённые_формулировки}}

                Верни ТОЛЬКО JSON в формате:
                ```json
                {"remarks":[{"category":"...","quote":"<точный фрагмент из текста>","suggestion":"<доработанный текст>","explanation":"<в чём проблема>"}]}
                ```
                Категории: «Орфография», «Грамматика», «Пунктуация», «Стиль», «Канцелярит», «Вода», «ИИ-штамп», «Запрещённая формулировка». Поле quote — дословный фрагмент из текста. Если фрагмент нужно просто удалить (например, «воду»), оставь suggestion пустой строкой "" — не пиши слово «удалить» как замену.
                """,
                temperature: 0.3
            )
        case .images:
            // .images is a StageKind.action stage: it opens ImagesView instead of
            // running a chat completion, and StageTemplateSeeder never seeds a
            // template for it, so this case is unreachable in practice.
            return StageTemplateContent(userPromptTemplate: "")
        case .promptAnalysis:
            return StageTemplateContent(
                userPromptTemplate: """
                Проанализируй, как система промтов справляется с написанием этой статьи, и предложи улучшения.

                Версии текста по этапам (последние принятые):
                {{история_версий_по_этапам}}

                Текущие промты этапов:
                {{текущие_промты_этапов}}

                Найди повторяющиеся проблемы в тексте, которые можно устранить, изменив промт этапа, а не вручную правя каждую статью. Объединяй похожие проблемы в одну рекомендацию.

                Верни ТОЛЬКО JSON в формате:
                ```json
                {"recommendations":[{"problem":"<проблема или объединённая группа проблем>","location":"<какой этап/промт это касается>","suggestion":"<конкретное предложение по правке промта>"}]}
                ```
                """,
                temperature: 0.3
            )
        }
    }
}
