import Foundation
import SwiftData

enum StageTemplateSeeder {
    @MainActor
    static func seedIfNeeded(in context: ModelContext) {
        let existing = (try? context.fetch(FetchDescriptor<StageTemplate>())) ?? []
        let seededStages = Set(existing.map { $0.stageRaw })

        for stage in PipelineStage.allCases where !seededStages.contains(stage.rawValue) {
            let template = makeTemplate(for: stage)
            context.insert(template)
        }
    }

    private static func makeTemplate(for stage: PipelineStage) -> StageTemplate {
        switch stage {
        case .draft:
            return StageTemplate(
                stage: .draft,
                systemPrompt: """
                Ты — медицинский редактор-копирайтер. Пиши достоверно, без искажения фактов, \
                с доказательной осторожностью. Соблюдай читабельность. Не выдумывай имена врачей, \
                процедуры и цифры — используй только переданные данные. Отвечай на русском в формате Markdown.
                """,
                userPromptTemplate: """
                Напиши черновик SEO-статьи.

                Тема: {{тема}}
                Тип статьи: {{тип}}
                Целевой объём (знаков): {{объём}}
                Направление: {{направление}}
                Данные врача: {{врач_данные}}
                Преимущества клиники: {{преимущества}}
                Приоритетные источники: {{источники_направления}}

                Сделай структуру H1/H2/H3 и связный текст. Не добавляй рекламных преувеличений.
                """
            )
        case .productBlocks:
            return StageTemplate(
                stage: .productBlocks,
                systemPrompt: """
                Ты — медицинский редактор. Встраиваешь продуктовые блоки клиники в существующий текст, \
                сохраняя структуру и стиль. Данные берёшь только из переданных. Русский, Markdown.
                """,
                userPromptTemplate: """
                Встрой выбранные продуктовые блоки в текст, не ломая его структуру.

                Текущий текст:
                {{текущий_текст}}

                Преимущества клиники: {{преимущества}}
                Данные врача: {{врач_данные}}

                Верни полный обновлённый текст статьи.
                """
            )
        case .semanticsInText:
            return StageTemplate(
                stage: .semanticsInText,
                systemPrompt: """
                Ты — SEO-редактор. Естественно встраиваешь ключевые запросы в текст без порчи русского языка \
                и без переспама. Не меняешь факты. Русский, Markdown.
                """,
                userPromptTemplate: """
                Встрой ключевые запросы в текст естественно. Учитывай частотность и обязательность. \
                Допиши/поправь H1, сгенерируй Title и Description.

                Текущий текст:
                {{текущий_текст}}

                Ключевые запросы:
                {{семантика}}

                После основного текста статьи добавь блок метаданных строго в таком формате:
                ```json
                {"h1":"...","seoTitle":"...","seoDescription":"...","embeddedQueries":["..."],"notes":"..."}
                ```
                """
            )
        case .seoCheck:
            return StageTemplate(
                stage: .seoCheck,
                systemPrompt: """
                Ты — придирчивый SEO-редактор медицинских статей. Твоя задача — не переписывать текст, \
                а найти проблемы и предложить точечные правки. Проверяй: структуру заголовков H1/H2/H3, \
                наличие и естественность ключевых запросов и их плотность, корректность Title и Description \
                (длину), объём, отсутствие рекламных преувеличений. Русский язык. Сомневайся и сверяй.
                """,
                userPromptTemplate: """
                Проверь текст на соответствие SEO-требованиям. Не переписывай — верни список замечаний.

                Текст:
                {{текущий_текст}}

                Ключевые запросы:
                {{семантика}}

                Верни ТОЛЬКО JSON в формате:
                ```json
                {"remarks":[{"category":"...","quote":"<точный фрагмент из текста>","suggestion":"<замена>","explanation":"<что не так и почему>"}]}
                ```
                Категории: «Заголовки», «Ключи», «Title/Description», «Объём», «Рекламность». \
                Поле quote — дословный фрагмент из текста (для поиска). Если правка не нужна, suggestion оставь пустым.
                """
            )
        case .factCheck:
            return StageTemplate(
                stage: .factCheck,
                systemPrompt: """
                Ты — придирчивый медицинский фактчекер. Не переписывай текст — сверяй факты с переданным \
                справочником клиники и опирайся на доказательность. Проверяй имена врачей, названия процедур, \
                цены, числа и проверяемые утверждения. Если факт расходится со справочником — это замечание. \
                Не выдумывай. Русский язык.
                """,
                userPromptTemplate: """
                Сверь факты в тексте со справочником клиники. Не переписывай — верни список замечаний.

                Текст:
                {{текущий_текст}}

                Справочник клиники (База знаний):
                {{база_знаний}}

                Верни ТОЛЬКО JSON в формате:
                ```json
                {"remarks":[{"category":"...","quote":"<точный фрагмент из текста>","suggestion":"<исправление>","explanation":"<расхождение со справочником>"}]}
                ```
                Категории: «Имя врача», «Процедура», «Цена», «Факт». \
                Поле quote — дословный фрагмент из текста. Если правка не нужна, suggestion оставь пустым.
                """
            )
        case .finalReview:
            return StageTemplate(
                stage: .finalReview,
                systemPrompt: """
                Ты — придирчивый литературный редактор-корректор медицинских текстов. Не переписывай весь текст — \
                находи конкретные проблемы и предлагай точечные правки: орфография, грамматика, пунктуация, \
                стиль, канцеляризмы, перегруженные предложения, «вода». Соблюдай редполитику: ясность, \
                доказательная осторожность, без рекламных штампов. Русский язык.
                """,
                userPromptTemplate: """
                Вычитай текст. Не переписывай целиком — верни список точечных замечаний.

                Текст:
                {{текущий_текст}}

                Верни ТОЛЬКО JSON в формате:
                ```json
                {"remarks":[{"category":"...","quote":"<точный фрагмент из текста>","suggestion":"<исправленный фрагмент>","explanation":"<что не так>"}]}
                ```
                Категории: «Орфография», «Грамматика», «Пунктуация», «Стиль», «Канцелярит», «Вода». \
                Поле quote — дословный фрагмент из текста.
                """
            )
        }
    }
}
