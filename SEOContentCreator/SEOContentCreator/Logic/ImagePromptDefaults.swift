import Foundation

enum ImagePromptDefaults {
    static func content(for kind: ImagePromptKind) -> String {
        switch kind {
        case .cover:
            return """
            Тема статьи: {{тема}}

            Опиши один объект для обложки медицинской статьи, который однозначно передаёт тему: орган, анатомическую структуру, медицинский инструмент или понятный символ темы. Объект выполнен из прозрачного стекла; внутри допустимы мелкие поясняющие детали (камни, сосуды, внутренняя структура).

            Если тема неконкретная или её буквальная визуализация затрагивает половые органы или женскую грудь (например, циркумцизио), предложи вместо этого абстрактный деликатный стеклянный объект, передающий тему косвенно: обобщённую форму, символ здоровья или защиты.
            """
        case .illustration:
            return """
            Тема статьи: {{тема}}
            Нужно проиллюстрировать эту часть текста:
            {{выделенный_фрагмент}}

            Опиши один понятный объект или простую предметную сцену, передающую смысл фрагмента. Объект выполнен из прозрачного стекла; внутри допустимы мелкие поясняющие детали.

            Если фрагмент подразумевает изображение половых органов или женской груди, предложи абстрактный деликатный стеклянный объект, передающий смысл косвенно.
            """
        }
    }
}
